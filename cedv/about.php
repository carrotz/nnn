			<h3>网站建设背景</h3>
			<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;20世纪70年代以来，恶性肿瘤的发病率及病死率均呈现上升趋势，恶性肿瘤成为广泛关注的焦点，肿瘤研究也进入了大数据时代，这给恶性肿瘤流行病学的相关研究带来诸多困难，因此急需一种将数据更直观地展现出来的方式。
<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;可视化是利用计算机图形学和图像处理技术，通过数据可视化技术可以从大量的数据集构成数据图像，可以从不同的维度观察数据，对数据进行更深入的观察和分析。

		  </p>
			<h3>网站建设目的</h3>
			<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本网站通过可视化方式将具体的肿瘤发病数据直观地展示在用户面前，用户无需再面对大量数据就可直接获得所想要的信息，无论是科研工作者还是普通用户都可以轻松使用。
		  </p>
			<h3>网站功能介绍</h3>
			<p>
				&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;本网站用雷达图直观展示了前十位高发肿瘤、饼图展示每年前九位高发肿瘤，而且还用 bar图展示前十位高发肿瘤与年龄关系、箱式图展示每年肿瘤发病与年龄的关系，用折线图展示每种肿瘤每年发病数。
<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;除了要求以外，本网站还增加了许多创新点，不仅在首页加入了动态可视化效果分析前九位高发肿瘤的伞状图，而且针对用户的需求做出了许多界面友好的新功能，如在交互性上提供了肿瘤发病位置图、Echart数据透视与保存、快速定位功能，检索方面提供了多入口词及全库搜索，同时利用了Treemap、横断面和出生队列等进行多维度展示及利用Google Trends的年搜索量、PubMed和SinoMed年发文量和对肿瘤的整体和具体的肿瘤做了趋势对比分析。

		  </p>
            
